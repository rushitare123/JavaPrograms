package ArraysProgram;

public class ArrSimpleForEachLoop {

	public static void main(String[] args) {
		
		String b[] = {"ram ,sham ,raj ,rushi ,mahesh"};
		int a[] = {10 ,20, 30,40 ,50};
		
		for(int d:a) {
			System.out.println(d);
		}
		
		for(String s:b) {
		System.out.println(b);	
		}
	}

}
